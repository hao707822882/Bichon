import redis


class RedisPublisher(object):
    def __init__(self, host, topic, **arg):
        self.topic = topic
        self.rc = redis.Redis(host=host, **arg)
        ps = self.rc.pubsub()
        ps.subscribe(topic)

    def send(self, msg):
        self.rc.publish(self.topic, msg)
